
public class FirstCode {
	public static void main(String args[]) {

		/*
		 * short temp, num = 5; temp = ++num; System.out.println(temp);
		 * System.out.println(num);
		 */

		int n = 10, i, j;
		for (i = 1; i <= n; i++) {
			for (j = 1; j <= i; j++) {
				System.out.print("*");
			}

			System.out.println();
		}

	}

}
